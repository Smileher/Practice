//结点的分类
//结点拥有的子树的个数称为结点的度(Degree)，整个树的度为树内各结点的度的最大值
//度为0的结点称为叶结点(Leaf)或终端结点，度不为0的结点称为分支结点或非终端结点
//除根结点外，分支结点也称为内部结点

//结点的关系
//结点的子树的根称为结点的孩子(Child)，相应的，该结点称为孩子的双亲(Parent)
//同一双亲的孩子之间互称为兄弟(Sibling)
//结点的祖先是从根到该结点所经分支上的所有结点

//结点的层次
//结点的层次(Level)从根开始，根为第一层，根的孩子为第二层
//其双亲在同一层的结点互为堂兄弟
//树中结点的最大层次称为树的深度(Depth)或高度

//其它概念
//如果将树中结点的各子树看成从左至右是有次序的，不能互换的，则称该树为有序树，反之无序树
//森林(Forest)是m(m>=0)棵互不相交的树的集合，对树中每个结点而言，其子树的集合即为森林

